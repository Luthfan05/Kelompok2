    public void show_data(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("Nama");
        tabel.addColumn("Kode Kamar");
        tabel.addColumn("TTL");
        tabel.addColumn("Alamat");
        tabel.addColumn("Tanggal Masuk");
        tabel.addColumn("Periode");
        tabel.addColumn("No Telepon");
        tabel.addColumn("No Telepon Wali");
        try {
            java.sql.Connection con = (java.sql.Connection)Home.Koneksi.getKoneksi();
            String sql = "SELECT * FROM penghuni";
            java.sql.PreparedStatement stm = con.prepareStatement(sql);
            ResultSet rs =stm.executeQuery(sql);
            while (rs.next()) {
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                    rs.getString(8)});
                }
            jTable1.setModel(tabel);
            } catch (Exception e){
                
        }
    }
